        VistaDistancias abrir2 = new VistaDistancias();        
        if(captura=="Divisas"){            
            abrir.setVisible(true);
            this.setVisible(true);
            abrir2.setVisible(false);
        }else if(captura=="Distancia"){
            abrir.setVisible(false);
            abrir2.setVisible(true);
            this.setVisible(true);
        }
        
